/**
 * Definition for binary tree
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    int maxDepth(TreeNode *root) {
        if (root){
            int leftD= maxDepth(root->left)+1;
            int rightD= maxDepth(root->right)+1;
            return leftD > rightD ? leftD:rightD;
        }
        else return 0;
    }
};

